"""Practicing Numeric Operators!"""

__author__ = "730316359"

Left_hand_side: str = input("Left-hand side: ")
Right_hand_side: str = input("Right-hand side: ")
L: int = int(Left_hand_side)
R: int = int(Right_hand_side)
print((Left_hand_side) + " ** " + (Right_hand_side) + " is " + str(L ** R))
print((Left_hand_side) + " / " + (Right_hand_side) + " is " + str(L / R))
print((Left_hand_side) + " // " + (Right_hand_side) + " is " + str(L // R))
print((Left_hand_side) + " % " + (Right_hand_side) + " is " + str(L % R))